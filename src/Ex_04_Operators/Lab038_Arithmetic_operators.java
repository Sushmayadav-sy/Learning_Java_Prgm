package Ex_04_Operators;

public class Lab038_Arithmetic_operators {
    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder


        int a = 20;
        int b = 3;
        float c = 3.0f;


        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c); // here it takes the float number
        System.out.println(a/b); // here it considers only integer value and do not take decimal value





    }

}

